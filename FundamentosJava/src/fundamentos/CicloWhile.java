package fundamentos;

public class CicloWhile {
    public static void main(String []args) {
        // while = mientras se cumpla la condicion
        int i=10;
        //< signo menor que            i++;

        //> signo mayor que
        while(i<10){
            System.out.println("valor: " + i);
            i++;
        }       
    }
}
